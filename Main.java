public class Main {
    public static void main(String[] args) {
        // Simple Calculator
        int a = 10;
        int b = 5;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        
        // Additional operations
        System.out.println("a^2 = " + (a * a));
        System.out.println("b^2 = " + (b * b));
    }
}